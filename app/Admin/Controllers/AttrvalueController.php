<?php

namespace App\Admin\Controllers;

use App\Http\Controllers\Controller;
use App\Models\Attrvalue;
use App\Models\Catattr;
use Encore\Admin\Controllers\HasResourceActions;
use Encore\Admin\Form;
use Encore\Admin\Grid;
use Encore\Admin\Layout\Content;
use Encore\Admin\Show;

class AttrvalueController extends Controller {
	use HasResourceActions;

	/**
	 * Index interface.
	 *
	 * @param Content $content
	 * @return Content
	 */
	public function index(Content $content) {
		return $content
			->header('Index')
			->description('description')
			->body($this->grid());
	}

	/**
	 * Show interface.
	 *
	 * @param mixed $id
	 * @param Content $content
	 * @return Content
	 */
	public function show($id, Content $content) {
		return $content
			->header('Detail')
			->description('description')
			->body($this->detail($id));
	}

	/**
	 * Edit interface.
	 *
	 * @param mixed $id
	 * @param Content $content
	 * @return Content
	 */
	public function edit($id, Content $content) {
		return $content
			->header('Edit')
			->description('description')
			->body($this->form()->edit($id));
	}

	/**
	 * Create interface.
	 *
	 * @param Content $content
	 * @return Content
	 */
	public function create(Content $content) {
		return $content
			->header('Create')
			->description('description')
			->body($this->form());
	}

	/**
	 * Make a grid builder.
	 *
	 * @return Grid
	 */
	protected function grid() {
		$grid = new Grid(new Attrvalue);

		$grid->id('ID')->sortable();
		$grid->attrvalue('属性值')->editable();
        $grid->catattr()->name('属性')->color('#f00')->sortable();
        $grid->order('排序')->editable();
		$grid->status('状态')->using(['1' => '是', '0' => '否']);
		// $grid->created_at('Created at');
		// $grid->updated_at('Updated at');

		return $grid;
	}

	/**
	 * Make a show builder.
	 *
	 * @param mixed $id
	 * @return Show
	 */
	protected function detail($id) {
		$show = new Show(Attrvalue::findOrFail($id));

		$show->id('ID');
        $show->catattr()->name();
		$show->attrvalue();
		// $show->created_at('Created at');
		// $show->updated_at('Updated at');

		return $show;
	}

	/**
	 * Make a form builder.
	 *
	 * @return Form
	 */
	protected function form() {
		$form = new Form(new Attrvalue);

		$form->display('ID');
        $form->select('catattr_id')->options(Catattr::all()->pluck('name','id'));
		$form->text('attrvalue');
        $states = [
            'on'  => ['value' => 1, 'text' => '打开', 'color' => 'success'],
            'off' => ['value' => 0, 'text' => '关闭', 'color' => 'danger'],
        ];
        $form->text('order');
        $form->switch('status')->states($states);

		// $form->text('status');
		// $form->display('Created at');
		// $form->display('Updated at');

		return $form;
	}
}
